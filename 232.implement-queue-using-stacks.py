class MyQueue:

    def __init__(self):
        self.q = []

    def push(self, x: int) -> None:
        s = []
        while self.q:
            s.append(self.q.pop())
        self.q.append(x)
        while s:
            self.q.append(s.pop())

    def pop(self) -> int:
        return self.q.pop()

    def peek(self) -> int:
        return self.q[-1]

    def empty(self) -> bool:
        return not self.q


